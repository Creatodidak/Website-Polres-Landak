        <h3><b>SATUAN RESERSE KRIMINAL POLRES LANDAK</b></h3>
    </div>
    <div class="col-md-8 offset-md-2" style="text-align: justify;margin-top: 20px;border-left: 10px solid red">
Satuan Reserse Kriminal yang selanjutnya disingkat Sat Reskrim adalah unsur pelaksana tugas pokok fungsi Reserse Kriminal pada tingkat Polres yang berada dibawah Kapolres. Sat Reskrim bertugas melaksanakan penyelidikan, penyidikan, dan pengawasan penyidikan  tindak  pidana,  termasuk  fungsi  identifikasi  dan  laboratorium forensik lapangan serta pembinaan, koordinasi dan pengawasan PPNS.
<br><br>
Dalam melaksanakan tugas sebagaimana dimaksud pada ayat (2), Sat Reskrim menyelenggarakan fungsi:
<br><br>
<ul style="list-style-type: decimal;">
<li>Pembinaan teknis terhadap administrasi penyelidikan dan penyidikan, serta identifikasi dan laboratorium forensik lapangan</li>
<li>Pelayanan dan perlindungan khusus kepada remaja, anak, dan wanita baik sebagai pelaku maupun korban sesuai dengan ketentuan peraturan perundang-undangan</li>
<li>Pengidentifikasian  untuk  kepentingan  penyidikan  dan  pelayanan umum</li>
<li>Penganalisisan   kasus   beserta   penanganannya,   serta   mengkaji efektivitas pelaksanaan tugas Satreskrim</li>
<li>Pelaksanaan pengawasan penyidikan tindak pidana yang dilakukan oleh penyidik pada unit reskrim Polsek dan Satreskrim Polres</li>
<li>Pembinaan, koordinasi dan pengawasan PPNS baik di bidang operasional maupun administrasi penyidikan sesuai dengan ketentuan peraturan perundang-undangan</li>
<li>Penyelidikan dan penyidikan tindak pidana umum dan khusus, antara lain tindak pidana ekonomi, korupsi, dan tindak pidana tertentu di daerah hukum Polres.
</li>
</ul>
 <br>

Sat Reskrim dalam melaksanakan tugas dibantu oleh :

 <br><br>
<ul style="list-style-type: decimal;">
<li>Urusan Pembinaan Operasional ( Urbinopsnal) , yang bertugas melakukan pembinaan dan pengawasan terhadap administrasi serta pelaksanaan penyelidikan dan penyidikan, menganalisis penanganan kasus dan mengevaluasi efektivitas pelaksanaan tugas Satreskrim</li>
<li>Urusan Administrasi dan Ketatausahan (Urmintu), yang bertugas menyelenggarakan kegiatan administrasi dan ketatausahaan</li>
<li>Unit, terdiri dari 4 (empat) Unit, yang bertugas melakukan penyelidikan dan penyidikan tindak pidana umum dan khusus antara lain tindak pidana ekonomi, korupsi, dan tindak pidana tertentu di daerah hukum Polres serta memberikan pelayanan dan perlindungan khusus kepada remaja, anak, dan wanita baiksebagai pelaku maupun korban sesuai dengan ketentuan peraturan perundang-undangan;</li>
<li>Urusan Identifikasi (Urident) yang bertugas melakukan identifikasi dan laboratorium forensic lapangan dan pengidentifikasian untuk kepentingan penyidikan dan pelayanan umum.</li>
</ul>


        
