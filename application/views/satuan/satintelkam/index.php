	<div class="col-md-8 offset-md-2" style="text-align: justify;margin-top: 20px;border-left: 10px solid yellow">
	

	Sat Intelkam adalah unsur pelaksana utama Polres yang berada di bawah Kapolres.<br/>
Sat Intelkam bertugas menyelenggarakan / membina fungsi Intelejen bidang Keamanan, termasuk persandian, dan pemberi pelayanan dalam bentuk Surat izin / Keterangan yang menyangkut Orang Asing, Senjata Api & Bahan Peledak, kegiatan social / Politik masyarakat dan Surat Keterangan Catatan Kepolisian (SKCK) kepada warga masyarakat yang membutuhkan serta melakukan pengawasan / pengamanan dan pelaksanaannya.<br/>
Sat Intelkam dipimpin oleh Kepala Sat Intelkam, disingkat Kasat Intelkam yang bertanggung jawab kepada Kapolres dan dalam pelaksanaan tugas sehari-hari di bawah kendali Waka Polres.<br/>

<br>Sat Intelkam terdiri dari Urusan Administrasi dan Ketatausahaan, serta sejumlah Unit diantaranya :<br/>

<UL style="list-style-type: decimal;">
<li>Urusan Pembinaan Operasi disingkat Urbin Ops</li>
<li>Urusan Sandi bertugas melaksanakan giat Persandian</li>
<li>Urusan Yanmin bertugas melaksanakan Pelayanan Administrasi</li>
</ul>
<br><b>Unit Opsnal :</b><br/>
<UL style="list-style-type: decimal;">
<li>Unit Bidang Sosial Ekonomi</li>
<li>Unit Bidang Sosial Budaya</li>
<li>Unit Bidang Keamanan</li>
<li>Unit Bidang Politik</li>
<li>Wasendak (Pengawasan Senjata dan Bahan Peladak)</li>
<li>POA (Pengawasan Orang asing)</li><br/>
</ul>

<b>Tugas Pokok</b><br/>
<UL style="list-style-type: decimal;">
<li>Sebagai Mata dan Telinga kesatuan Polri yang berkewajiban melaksanakn deteksi dini dan memberikan peringatan masalah dan perkembangan masalah dan perubahan kehidupan sosial dalam masyarakat. Mengidentifikasi ancaman, gangguan, atau hambatan terhadap Kamtibmas.</li>
<li>Melaksanakan pengamatan terhadap sasaran-sasaran tertentu dalam masyarakat di bedang Ipoleksosbudhankam bagi kepentingan yang membahayakan masyarakat khususnya dalam kegiatan kontra Intelijen;</li>
<li>Menciptakan kondisi tertentu yang menguntungkan dalam masyarakat bagi pelakasanaan tugas Polri.</li>
</ul>
<br/>
<b>Fungsi</b><br/>
Penyelidikan, Pengamanan dan penggalangan untuk keperluan pelaksanaan tugas dan fungsi kepolisian, terutama penegakan hukum, pembinaan kamtibmas, serta keperluan tugas bantuan pertahanan dan kekuatan sosial.

		
