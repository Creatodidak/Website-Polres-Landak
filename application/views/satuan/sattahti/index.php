Sat Tahti sebagaimana dimaksud dalam Perkap 23 Tahun 2010 Pasal 10 huruf j merupakan unsur pelaksana tugas pokok yang berada di bawah Kapolres.<br/><br/>

Sattahti bertugas menyelenggarakan perawatan tahanan meliputi pelayanan kesehatan tahanan, pembinaan tahanan serta menerima, menyimpan, dan mengamankan barang bukti beserta administrasinya di lingkungan Polres, melaporkan jumlah dan kondisi tahanan sesuai dengan ketentuan peraturan perundang-undangan.<br/><br/>

Dalam melaksanakan tugas sebagaimana dimaksud pada ayat (2), Sattahti menyelenggarakan fungsi:
<ul style="list-style-type: decimal;">
    <li>pembinaan dan pemberian petunjuk tata tertib yang berkaitan dengan tahanan, yang meliputi pemeriksaan fasilitas ruang tahanan, jumlah dan kondisi tahanan beserta administrasinya;</li>
    <li>pelayanan kesehatan, perawatan, pembinaan jasmani dan rohani tahanan;</li>
    <li>pengelolaan barang titipan milik tahanan; dan</li>
    <li>pengamanan dan pengelolaan barang bukti beserta administrasi</li>
</ul>
<br/><br/>
Sattahti dipimpin oleh Kasattahti yang bertanggung jawab kepada Kapolres dan dalam pelaksanaan tugas sehari-hari di bawah kendali Wakapolres.
